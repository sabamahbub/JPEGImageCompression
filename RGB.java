public class RGB{
    public int R, G, B;

    public RGB(int R, int G, int B){
        this.R = R;
        this.G = G;
        this.B = B;
    }

    public void setR(int R){
        this.R = R;
    }
    public void setG(int G){
        this.G = G;
    }
    public void setB(int B){
        this.B = B;
    }
    public int getR(){
        return this.R;
    }
    public int getG(){
        return this.G;
    }
    public int getB(){
        return this.B;
    }

}